#!/usr/bin/perl

use strict;
use warnings;

my @crypt = (0x94,0x88,0x81,0x85,0x97,0x81,0xc4,0x97,0x91,0x86,0x89,0x8d,0x90,
             0xc4,0xc6,0x8c,0x85,0x87,0x8f,0x85,0x80,0x8b,0x8b,0x80,0x88,0x81,
             0xc6,0xc4,0x82,0x8b,0x96,0xc4,0x90,0x8c,0x8d,0x97,0xc4,0x87,0x8c,
             0x85,0x88,0x88,0x81,0x8a,0x83,0x81);

count:
for(my $i = 0; $i < 256; $i++) {
    my $plain = "";

    foreach(@crypt) {
        my $char = $i ^ $_;
        
        if ($char >= 32 && $char <= 126) {
            $plain .= chr($char);
        } else {
            next count;
        }
    }
    
    if($plain =~ / /) {
        print("$i: $plain\n");
    }
}